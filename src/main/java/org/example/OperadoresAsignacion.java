package org.example;

public class OperadoresAsignacion {

    public static void main(String[] args) {

        int a = 3;

        a += 1;
        System.out.println("a = " + a);

        a += 3;
        System.out.println("a = " + a);

        a -= 1;
        System.out.println("a = " + a);

        a *= 2;
        System.out.println("a = " + a);
        a *= 3;
        System.out.println("a = " + a);

        a /= 3;
        System.out.println("a = " + a);

        a %= 5;
        System.out.println("a = " + a);
    }
}
